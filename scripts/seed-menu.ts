async function seedMenuItems() {
  try {
    await connectDB()
    console.log('Connected to MongoDB')

    // Clear existing menu items
    await MenuItem.deleteMany({})
    console.log('Cleared existing menu items')

    // Insert new menu items
    const result = await MenuItem.insertMany(menuItems)
    console.log(`‚úÖ Successfully seeded ${result.length} menu items`)

    // Show summary
    const summary = await MenuItem.aggregate([
      {
        $group: {
          _id: '$restaurantId',
          count: { $sum: 1 },
          categories: { $addToSet: '$category' },
        },
      },
    ])

    console.log('\nüìä Menu Items Summary:')
    summary.forEach((item) => {
      console.log(`   ${item._id}: ${item.count} items (${item.categories.join(', ')})`)
    })

    process.exit(0)
  } catch (error) {
    console.error('‚ùå Error seeding menu items:', error)
    process.exit(1)
  }
}

seedMenuItems()
